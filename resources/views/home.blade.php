    // Toast notification system
    function showToast(message, type = 'info') {
      const toast = document.createElement('div');
      toast.className = `uc-toast uc-toast--${type}`;
      toast.innerHTML = `
        <div class="uc-toast__icon">${type === 'error' ? '❌' : type === 'success' ? '✓' : 'ℹ️'}</div>
        <div class="uc-toast__message">${message}</div>
      `;
      document.body.appendChild(toast);
      
      setTimeout(() => toast.classList.add('uc-toast--show'), 100);
      setTimeout(() => {
        toast.classList.remove('uc-toast--show');
        setTimeout(() => toast.remove(), 300);
      }, 5000);
    }

    // Player ID validation
    function validatePlayerId(id) {
      if (!id || id.trim().length === 0) {
        showToast('Введите Игровой ID', 'error');
        return false;
      }
      
      if (!/^5\d{4,13}$/.test(id.trim())) {
        showToast('ID должен начинаться с 5 и содержать от 5 до 14 цифр', 'error');
        return false;
      }
      
      return true;
    }

    // Save Player ID to localStorage
    function savePlayerId(id) {
      try {
        localStorage.setItem('lastPlayerId', id);
        localStorage.setItem('lastPlayerIdDate', new Date().toISOString());
      } catch (e) {
        console.warn('Cannot save to localStorage:', e);
      }
    }

    // Load Player ID from localStorage
    function loadPlayerId() {
      try {
        const savedId = localStorage.getItem('lastPlayerId');
        const savedDate = localStorage.getItem('lastPlayerIdDate');
        
        if (savedId && savedDate) {
          const daysSinceLastUse = (Date.now() - new Date(savedDate)) / (1000 * 60 * 60 * 24);
          if (daysSinceLastUse < 30) {
            const input = document.querySelector('.uc-id-form__input');
            if (input && !input.value) {
              input.value = savedId;
              input.classList.add('uc-input-autofilled');
            }
          }
        }
      } catch (e) {
        console.warn('Cannot load from localStorage:', e);
      }
    }

    // Checkout function
    window.proceedToCheckout = async function() {
      const btn = document.querySelector('.uc-id-form__submit');
      const gameIdInput = document.querySelector('.uc-id-form__input');
      
      // Validation
      if (Object.keys(window.cart).length === 0) {
        showToast('Корзина пуста! Выберите товары', 'error');
        return;
      }
      
      const gameId = gameIdInput.value.trim();
      if (!validatePlayerId(gameId)) {
        gameIdInput.focus();
        return;
      }
      
      // Disable button and show loading
      btn.disabled = true;
      const originalText = btn.innerHTML;
      btn.innerHTML = '<span class="uc-spinner"></span> Обработка...';
      
      try {
        // Get CSRF token
        const csrfToken = document.querySelector('meta[name="csrf-token"]')?.content || '{{ csrf_token() }}';
        
        // Calculate total amount and collect products
        let totalAmount = 0;
        let cartItems = []; // Полная информация о корзине
        
        Object.keys(window.cart).forEach(uc => {
          const qty = window.cart[uc]; // Количество товара
          
          // Map UC to product ID (from packages data)
          const packageCard = document.querySelector(`[data-uc="${uc}"]`);
          if (packageCard) {
            const price = parseInt(packageCard.dataset.price);
            const productId = packageCard.dataset.productId || 1;
            const ucAmount = parseInt(uc);
            
            totalAmount += price * qty;
            
            cartItems.push({
              uc: ucAmount,
              product_id: productId,
              quantity: qty,
              price: price
            });
          }
        });
        
        console.log('Total amount:', totalAmount, 'Cart items:', cartItems);
        
        // Step 1: Create Order
        const orderResponse = await fetch('/order/add', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Accept': 'application/json',
            'X-CSRF-TOKEN': csrfToken
          },
          body: JSON.stringify({
          product_id: cartItems[0].product_id,
          uid: gameId
        })
        });
        
        if (!orderResponse.ok) {
          const error = await orderResponse.json();
          throw new Error(error.message || 'Ошибка создания заказа');
        }
        
        const orderData = await orderResponse.json();
        
        if (!orderData.success || !orderData.id) {
          throw new Error('Invalid order response');
        }
        
        // Save Player ID for future use
        savePlayerId(gameId);
        
        // Step 2: Initialize Payment
        const paymentResponse = await fetch('/order/payment', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
