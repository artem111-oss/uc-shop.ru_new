document.addEventListener('DOMContentLoaded', function() {
    const reviewsTrack = document.getElementById('reviews-track');
    let currentSlide = 0;
    let carouselInterval = null;
    
    // Функция обновления позиции карусели
    function updateCarousel() {
        if (!reviewsTrack) return;
        
        const cards = reviewsTrack.querySelectorAll('.uc-review-card');
        if (cards.length === 0) return;
        
        // Вертикальная прокрутка по 1 отзыву
        const cardHeight = cards[0].offsetHeight;
        const gap = 16; // gap между карточками в px
        const offset = currentSlide * (cardHeight + gap);
        
        reviewsTrack.style.transform = `translateY(-${offset}px)`;
    }
    
    // Функция для прокрутки к следующему слайду
    function nextSlide() {
        if (!reviewsTrack) return;
        
        const cards = reviewsTrack.querySelectorAll('.uc-review-card');
        if (cards.length === 0) return;
        
        // Количество оригинальных отзывов (половина от всех, т.к. мы дублировали)
        const originalCount = cards.length / 2;
        
        currentSlide++;
        
        // Бесконечная прокрутка: если дошли до половины (конца оригинальных), возвращаемся к началу
        if (currentSlide >= originalCount) {
            // Мгновенно возвращаемся к началу без анимации
            reviewsTrack.style.transition = 'none';
            currentSlide = 0;
            reviewsTrack.style.transform = `translateY(0px)`;
            
            // Через малый таймаут возвращаем анимацию и двигаемся к следующему
            setTimeout(() => {
                reviewsTrack.style.transition = 'transform 0.6s ease-in-out';
                currentSlide = 1;
                updateCarousel();
            }, 50);
        } else {
            updateCarousel();
        }
    }
    
    // Загрузка отзывов из API
    async function loadReviews() {
        try {
            const response = await fetch('/api/reviews', {
                method: 'GET',
                headers: {
                    'Accept': 'application/json'
                }
            });
            
            if (!response.ok) {
                throw new Error('Failed to fetch reviews');
            }
            
            const data = await response.json();
            
            if (data.success && data.reviews) {
                renderReviews(data.reviews);
                console.log('✅ Reviews loaded:', data.count);
            }
        } catch (error) {
            console.error('❌ Failed to load reviews:', error);
        }
    }
    
    // Рендер отзывов в карусель
    function renderReviews(reviews) {
        if (!reviewsTrack) return;
        
        reviewsTrack.innerHTML = '';
        
        // Дублируем отзывы для бесконечной прокрутки (добавляем копию в конец)
        const allReviews = [...reviews, ...reviews];
        
        allReviews.forEach(review => {
            const card = document.createElement('div');
            card.className = 'uc-review-card';
            
            // Создаем звезды через DOM элементы
            const headerDiv = document.createElement('div');
            headerDiv.className = 'uc-review-card__header';
            
            const ratingSpan = document.createElement('span');
            ratingSpan.className = 'uc-review-card__rating';
            
            // Добавляем каждую звезду как отдельный элемент
            for (let i = 0; i < review.rating; i++) {
                const star = document.createElement('span');
                star.className = 'star';
                star.innerHTML = '&#9733;'; // HTML entity для звезды
                ratingSpan.appendChild(star);
            }
            
            const timeSpan = document.createElement('span');
            timeSpan.className = 'uc-review-card__time';
            timeSpan.textContent = review.time_ago;
            
            headerDiv.appendChild(ratingSpan);
            headerDiv.appendChild(timeSpan);
            
            const textP = document.createElement('p');
            textP.className = 'uc-review-card__text';
            textP.textContent = review.text;
            
            card.appendChild(headerDiv);
            card.appendChild(textP);
            
            reviewsTrack.appendChild(card);
        });
        
        // Перезапускаем карусель после загрузки отзывов
        currentSlide = 0;
        reviewsTrack.style.transition = 'transform 0.6s ease-in-out';
        reviewsTrack.style.transform = 'translateY(0)';
        
        // Очищаем старый интервал и запускаем новый
        if (carouselInterval) {
            clearInterval(carouselInterval);
        }
        carouselInterval = setInterval(nextSlide, 5000);
        
        console.log('✅ Reviews rendered:', reviews.length, '(duplicated to', allReviews.length, 'for infinite scroll)');
    }
    
    // Первоначальная загрузка
    loadReviews();
    
    // Обновляем отзывы каждые 2 минуты (для обновления таймеров)
    setInterval(loadReviews, 120000);
    
    console.log('✅ Dynamic reviews & carousel initialized');
});
