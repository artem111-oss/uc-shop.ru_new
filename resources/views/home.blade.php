    window.updateCart = function(uc, delta, price) {
        const MAX_ORDER_AMOUNT = 300000;
        
        if (!window.cart[uc]) window.cart[uc] = 0;
        
        // Рассчитываем новую общую сумму
        let newTotalPrice = 0;
        document.querySelectorAll('.uc-package-card').forEach(c => {
            const cUc = parseInt(c.dataset.uc);
            const cPrice = parseInt(c.dataset.price);
            const qty = (cUc === uc) ? (window.cart[cUc] || 0) + delta : (window.cart[cUc] || 0);
            if (qty > 0) {
                newTotalPrice += cPrice * qty;
            }
        });
        
        // Проверяем лимит
        if (delta > 0 && newTotalPrice > MAX_ORDER_AMOUNT) {
            showToast(`Максимальная сумма заказа: ${MAX_ORDER_AMOUNT.toLocaleString('ru-RU')} ₽`, 'error');
            return;
        }
        
        window.cart[uc] += delta;
        if (window.cart[uc] <= 0) delete window.cart[uc];
        
        const card = document.querySelector(`[data-uc="${uc}"]`);
        const qtyEl = card?.querySelector('.uc-calc-qty');
        if (qtyEl) qtyEl.textContent = window.cart[uc] || 0;
        
        let totalUc = 0, totalPrice = 0;
        document.querySelectorAll('.uc-package-card').forEach(c => {
            const cUc = parseInt(c.dataset.uc);
            const cPrice = parseInt(c.dataset.price);
            const qty = window.cart[cUc] || 0;
            totalUc += cUc * qty;
            totalPrice += cPrice * qty;
        });
        
        const totalUcEl = document.getElementById('cart-total-uc');
        const totalPriceEl = document.getElementById('cart-total-price');
        if (totalUcEl) totalUcEl.innerHTML = `<strong>${totalUc.toLocaleString('ru-RU')}</strong> <strong>UC</strong>`;
        if (totalPriceEl) totalPriceEl.textContent = totalPrice.toLocaleString('ru-RU') + ' ₽';
        
        console.log('✅ updateCart:', window.cart);
